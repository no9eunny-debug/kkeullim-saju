/**
 * 로딩 팁 배열에서 랜덤으로 하나를 반환합니다.
 */
export function getRandomTip(): string {
  const index = Math.floor(Math.random() * LOADING_TIPS.length);
  return LOADING_TIPS[index];
}

/**
 * 중복 없이 N개의 랜덤 팁을 반환합니다.
 */
export function getRandomTips(count: number): string[] {
  const shuffled = [...LOADING_TIPS].sort(() => Math.random() - 0.5);
  return shuffled.slice(0, Math.min(count, LOADING_TIPS.length));
}
