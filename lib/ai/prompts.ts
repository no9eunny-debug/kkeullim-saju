export function getSystemPrompt(
  category: string,
  depth: AnalysisDepth
): string {
  const cat = category as Category;
  const categoryPrompt = CATEGORY_PROMPTS[cat] || CATEGORY_PROMPTS.basic;
  const depthInstruction = DEPTH_INSTRUCTIONS[depth] || DEPTH_INSTRUCTIONS.summary;

  return `${BASE_PERSONA}

${depthInstruction}

${categoryPrompt}

## 응답 형식
- 마크다운 형식 사용 가능 (볼드, 리스트 등)
- 섹션을 명확히 나누되, 딱딱하지 않게
- 사주 용어에는 간단한 괄호 설명 추가
  예: 식신(食神, 표현력과 창의력의 별)
- 마지막에 반드시 다른 분석 카테고리를 자연스럽게 유도하는 질문을 넣으세요`;
}

// ---------------------------------------------------------------------------
// 후속 질문 프롬프트
// ---------------------------------------------------------------------------

export function getFollowUpPrompt(category: string, depth: string): string {
  const cat = category as Category;
  const followUp = FOLLOW_UP_MAP[cat] || FOLLOW_UP_MAP.basic;

  const suggestNames = followUp.suggest
    .map((s) => getCategoryDisplayName(s))
    .join(", ");

  return `이전 ${getCategoryDisplayName(cat)} 분석에 대한 후속 질문이에요.

## 규칙
- 이전 분석 내용을 기억하고 연결해서 답변
- 새로운 관점이나 더 깊은 인사이트 제공
- 반복하지 말고 확장/심화
- 현재 분석 깊이: ${depth}
- 자연스럽게 다음 추천 카테고리로 유도: ${suggestNames}
- 유도 메시지 참고: "${followUp.message}"`;
}

